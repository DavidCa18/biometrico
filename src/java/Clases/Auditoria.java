public class Auditoria {
    
    private String  tipoAuditoria;
    private String  fechaAuditoria;
    private String  tablaAuditoria;    
    private String  campoAuditoria;
    private String  anteriorAuditoria;
    private String  nuevoAuditoria;
    private String  usuarioAuditoria;

    public String getTipoAuditoria() {
        return tipoAuditoria;
    }

    public void setTipoAuditoria(String tipoAuditoria) {
        this.tipoAuditoria = tipoAuditoria;
    }

    public String getFechaAuditoria() {
        return fechaAuditoria;
    }

    public void setFechaAuditoria(String fechaAuditoria) {
        this.fechaAuditoria = fechaAuditoria;
    }

    public String getTablaAuditoria() {
        return tablaAuditoria;
    }

    public void setTablaAuditoria(String tablaAuditoria) {
        this.tablaAuditoria = tablaAuditoria;
    }

    public String getCampoAuditoria() {
        return campoAuditoria;
    }

    public void setCampoAuditoria(String campoAuditoria) {
        this.campoAuditoria = campoAuditoria;
    }

    public String getAnteriorAuditoria() {
        return anteriorAuditoria;
    }

    public void setAnteriorAuditoria(String anteriorAuditoria) {
        this.anteriorAuditoria = anteriorAuditoria;
    }

    public String getNuevoAuditoria() {
        return nuevoAuditoria;
    }

    public void setNuevoAuditoria(String nuevoAuditoria) {
        this.nuevoAuditoria = nuevoAuditoria;
    }

    public String getUsuarioAuditoria() {
        return usuarioAuditoria;
    }

    public void setUsuarioAuditoria(String usuarioAuditoria) {
        this.usuarioAuditoria = usuarioAuditoria;
    }
    
    
    
}
