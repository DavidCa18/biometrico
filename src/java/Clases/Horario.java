package Clases;

/**
 *
 * @author mario
 */
public class Horario {

    private int idHorario;
    private String turnoHorario;
    private String entradaHorario;
    private String salidaHorario;
    private String esperaHorario;

    public int getIdHorario() {
        return idHorario;
    }

    public void setIdHorario(int idHorario) {
        this.idHorario = idHorario;
    }

    public String getTurnoHorario() {
        return turnoHorario;
    }

    public void setTurnoHorario(String turnoHorario) {
        this.turnoHorario = turnoHorario;
    }

    public String getEntradaHorario() {
        return entradaHorario;
    }

    public void setEntradaHorario(String entradaHorario) {
        this.entradaHorario = entradaHorario;
    }

    public String getSalidaHorario() {
        return salidaHorario;
    }

    public void setSalidaHorario(String salidaHorario) {
        this.salidaHorario = salidaHorario;
    }

    public String getEsperaHorario() {
        return esperaHorario;
    }

    public void setEsperaHorario(String esperaHorario) {
        this.esperaHorario = esperaHorario;
    }
    
    

}
