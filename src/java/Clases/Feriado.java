package Clases;

/**
 *
 * @author LENOVO LEGION
 */
public class Feriado {

    private int idFeriado;
    private String fechaFeriado;
    private String fechaCompletaFeriado;
    private String anioFeriado;

    public int getIdFeriado() {
        return idFeriado;
    }

    public void setIdFeriado(int idFeriado) {
        this.idFeriado = idFeriado;
    }

    public String getFechaFeriado() {
        return fechaFeriado;
    }

    public void setFechaFeriado(String fechaFeriado) {
        this.fechaFeriado = fechaFeriado;
    }

    public String getFechaCompletaFeriado() {
        return fechaCompletaFeriado;
    }

    public void setFechaCompletaFeriado(String fechaCompletaFeriado) {
        this.fechaCompletaFeriado = fechaCompletaFeriado;
    }

    public String getAnioFeriado() {
        return anioFeriado;
    }

    public void setAnioFeriado(String anioFeriado) {
        this.anioFeriado = anioFeriado;
    }

}
