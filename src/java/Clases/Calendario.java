package Clases;

/**
 *
 * @author LENOVO LEGION
 */
public class Calendario {
    
    private String fechaCalendario;
    private String horaCalendario;
    private String tipoCalendario;
    private String estadoCalendario;
    private String observacionCalendario;

    public String getFechaCalendario() {
        return fechaCalendario;
    }

    public void setFechaCalendario(String fechaCalendario) {
        this.fechaCalendario = fechaCalendario;
    }

    public String getHoraCalendario() {
        return horaCalendario;
    }

    public void setHoraCalendario(String horaCalendario) {
        this.horaCalendario = horaCalendario;
    }

    public String getTipoCalendario() {
        return tipoCalendario;
    }

    public void setTipoCalendario(String tipoCalendario) {
        this.tipoCalendario = tipoCalendario;
    }

    public String getEstadoCalendario() {
        return estadoCalendario;
    }

    public void setEstadoCalendario(String estadoCalendario) {
        this.estadoCalendario = estadoCalendario;
    }

    public String getObservacionCalendario() {
        return observacionCalendario;
    }

    public void setObservacionCalendario(String observacionCalendario) {
        this.observacionCalendario = observacionCalendario;
    }
    
}
